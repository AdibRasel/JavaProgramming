package MyFirstJavaPackage; //======প্যাকেজের নাম


public class printinJava { // ====== ক্লাসের নাম

    public static void main(String[] args) { // ======= মেইন মেথড শুরু

        System.out.println("println = new line print"); // println দিয়ে একটা লাইনের পরে আরেকটা লাইন শুরু হবে  
        System.out.println("println = new line print");   
        System.out.println("println = new line print");     

        System.out.print("print = print one line,"); // print দিয়ে যত লাইন লিখবো সব এক লাইনে শো হবে।     
        System.out.print("print = print one line,");     


        System.out.print("print \t = one tab space,"); // \t ব্যাক স্লেস \t দিয়ে একটা ট্যাব স্পেস নিবে
        System.out.print("print \t = one tab space,");      

        System.out.println("print = new line \n"); // \n ব্যাক স্লেস \n দিয়ে নতুন একটা লাইন শুরু হবে
        System.out.println("print \n = new line,"); 
        System.out.println("print \n\n\n\n = 4 new line ");      


    } // ======== মেইন মেথড শেষ


}

